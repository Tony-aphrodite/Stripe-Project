            adminJsonOut(['ok' => false, 'error' => 'Email inválido.'], 400);
        }
        $base  = _cincelApiBase();
        $url   = $base . '/v3/tokens/otp';
        $authB64 = base64_encode($email . ':otp');
        [$http, $decoded, $raw, $cErr] = _cincelCall('GET', $url, [
            'Authorization: Basic ' . $authB64,
        ]);
        adminLog('cincel_bootstrap_request_otp', [
            'admin_id' => $adminId,
            'email'    => $email,
            'http'     => $http,
            'ok'       => $http >= 200 && $http < 300,
        ]);
        if ($http >= 200 && $http < 300) {
            adminJsonOut([
                'ok'      => true,
                'message' => 'Cincel envió un código OTP al correo ' . $email . '. Revísalo y úsalo en el paso 2.',
                'http'    => $http,
            ]);
        }
        adminJsonOut([
            'ok'           => false,
            'error'        => 'Cincel rechazó la solicitud de OTP (HTTP ' . $http . ').',
            'http'         => $http,
            'cincel_body'  => $decoded ?: $raw,
            'curl_err'     => $cErr,
            'url'          => $url,
            'hint'         => 'Verifica que CINCEL_API_URL y CINCEL_EMAIL sean correctos. Si Cincel devuelve 401, el correo no está registrado en su sistema.',
        ], 502);
    }

    // ── Action 2: Validate OTP, get JWT, set password, persist ──────────
    if ($action === 'set_password') {
        $email    = trim((string)($body['email']    ?? _cincelEmail()));
        $otpCode  = trim((string)($body['otp_code'] ?? ''));
        $password = (string)($body['password'] ?? '');

        if (!filter_var($email, FILTER_VALIDATE_EMAIL)) {
            adminJsonOut(['ok' => false, 'error' => 'Email inválido.'], 400);
        }
        if (!preg_match('/^[0-9]{4,8}$/', $otpCode)) {
            adminJsonOut(['ok' => false, 'error' => 'OTP debe ser un número de 4 a 8 dígitos.'], 400);
        }
        if (strlen($password) < 10) {
            adminJsonOut(['ok' => false, 'error' => 'La nueva contraseña debe tener al menos 10 caracteres.'], 400);
        }

        $base = _cincelApiBase();

        // ── Step B-1: GET /v3/tokens/jwt with email:OTP ──────────────────
        $jwtUrl = $base . '/v3/tokens/jwt';
        $authB64 = base64_encode($email . ':' . $otpCode);
        [$http1, $decoded1, $raw1, $err1] = _cincelCall('GET', $jwtUrl, [
            'Authorization: Basic ' . $authB64,
        ]);
        if ($http1 < 200 || $http1 >= 300) {
            adminLog('cincel_bootstrap_validate_otp_fail', ['admin_id'=>$adminId, 'http'=>$http1]);
            adminJsonOut([
                'ok'          => false,
                'error'       => 'No se pudo validar el OTP (HTTP ' . $http1 . '). Es muy probable que el código haya expirado — pide uno nuevo y vuelve a intentar.',
                'http'        => $http1,
                'cincel_body' => $decoded1 ?: $raw1,
                'curl_err'    => $err1,
                'step'        => 'validate_otp',
            ], 502);
        }

        // Extract the JWT. Cincel may return { "token": "..." } or { "jwt": "..." }
        // or a bare string — be defensive.
        $jwt = null;
        if (is_array($decoded1)) {
            foreach (['token','jwt','access_token','accessToken'] as $k) {
                if (!empty($decoded1[$k]) && is_string($decoded1[$k])) { $jwt = $decoded1[$k]; break; }
            }
        }
        if (!$jwt && is_string($raw1)) {
            $trim = trim($raw1, " \t\n\r\0\x0B\"");
            if (substr_count($trim, '.') === 2 && preg_match('#^[A-Za-z0-9_\-]+\.[A-Za-z0-9_\-]+\.[A-Za-z0-9_\-]+$#', $trim)) {
                $jwt = $trim;
            }
        }
        if (!$jwt) {
            adminJsonOut([
                'ok'          => false,
                'error'       => 'Cincel respondió 200 pero no encontramos el JWT en el body. Revisa la respuesta cruda.',
                'cincel_body' => $decoded1 ?: $raw1,
                'step'        => 'parse_jwt',
            ], 502);
        }

        // ── Step B-2: Decode JWT to extract user_id (sub claim) ─────────
        $payload = _jwtDecodePayload($jwt);
        $userId  = null;
        if (is_array($payload)) {
            foreach (['sub','user_id','userId','id'] as $k) {
                if (isset($payload[$k]) && $payload[$k] !== '') { $userId = $payload[$k]; break; }
            }
        }
        if (!$userId) {
            adminJsonOut([
                'ok'         => false,
                'error'      => 'JWT no contiene un campo sub (user_id) reconocible.',
                'jwt_claims' => $payload,
                'step'       => 'extract_user_id',
            ], 502);
        }

        // ── Step B-3: PATCH /v3/users/{user_id} with Bearer JWT ─────────
        $patchUrl  = $base . '/v3/users/' . rawurlencode((string)$userId);
        $patchBody = json_encode(['password' => $password]);
        [$http2, $decoded2, $raw2, $err2] = _cincelCall('PATCH', $patchUrl, [
            'Authorization: Bearer ' . $jwt,
            'Content-Type: application/json',
        ], $patchBody);
        if ($http2 < 200 || $http2 >= 300) {
            adminLog('cincel_bootstrap_set_password_fail', ['admin_id'=>$adminId, 'http'=>$http2, 'user_id'=>$userId]);
            adminJsonOut([
                'ok'          => false,
                'error'       => 'Cincel rechazó establecer la contraseña (HTTP ' . $http2 . ').',
                'http'        => $http2,
                'cincel_body' => $decoded2 ?: $raw2,
                'curl_err'    => $err2,
                'step'        => 'set_password',
            ], 502);
        }

        // ── Step B-4: Verify by getting a fresh JWT with email:password ──
        $verifyUrl = $base . '/v3/tokens/jwt';
        $verifyB64 = base64_encode($email . ':' . $password);
        [$http3, $decoded3, $raw3, $err3] = _cincelCall('GET', $verifyUrl, [
            'Authorization: Basic ' . $verifyB64,
        ]);
        $verifyOk = ($http3 >= 200 && $http3 < 300);

        // ── Step B-5: Persist to local-secrets.php ──────────────────────
        [$secretsPath, $writable] = _resolveSecretsFile();
        if (!$writable) {
            adminJsonOut([
                'ok'         => false,
                'error'      => 'La contraseña SE ESTABLECIÓ en Cincel correctamente, pero el servidor no pudo escribir local-secrets.php. La contraseña nueva es: ' . $password . ' — guárdala manualmente.',
                'password'   => $password,
                'verify_ok'  => $verifyOk,
                'step'       => 'persist',
            ], 500);
        }
        if (!_writeSecretsFile($secretsPath, $email, $password, $adminId)) {
            adminJsonOut([
                'ok'         => false,
                'error'      => 'La contraseña SE ESTABLECIÓ en Cincel pero falló la escritura del archivo. La contraseña nueva es: ' . $password . ' — guárdala manualmente.',
                'password'   => $password,
                'verify_ok'  => $verifyOk,
                'step'       => 'persist',
            ], 500);
        }

        adminLog('cincel_bootstrap_complete', [
            'admin_id'  => $adminId,
            'email'     => $email,
            'user_id'   => $userId,
            'verify_ok' => $verifyOk,
            'file'      => $secretsPath,
        ]);

        adminJsonOut([
            'ok'        => true,
            'message'   => '✅ Contraseña establecida y guardada. Cincel ahora puede autenticarse con email+password en cada llamada.',
            'user_id'   => $userId,
            'verify_ok' => $verifyOk,
            'file'      => $secretsPath,
        ]);
    }

    adminJsonOut(['ok' => false, 'error' => 'action desconocida'], 400);
}


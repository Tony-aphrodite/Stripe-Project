function _cincelApiBase(): string {
    @require_once __DIR__ . '/../../configurador/php/config.php';
    $u = defined('CINCEL_API_URL') ? rtrim(CINCEL_API_URL, '/') : 'https://api.cincel.digital/v3';
    // Some env values include /v3 already; strip it so we can compose paths cleanly.
    return preg_replace('#/v\d+$#', '', $u) ?: 'https://api.cincel.digital';
}

function _cincelEmail(): string {
    @require_once __DIR__ . '/../../configurador/php/config.php';
    return defined('CINCEL_EMAIL') ? CINCEL_EMAIL : (getenv('CINCEL_EMAIL') ?: '');
}

/**
 * Run a single HTTP call to Cincel. Returns [http_code, decoded_body, raw_body, curl_err].
 */
function _cincelCall(string $method, string $url, array $headers, ?string $body = null): array {
    $ch = curl_init($url);
    $opts = [
        CURLOPT_CUSTOMREQUEST  => strtoupper($method),
        CURLOPT_RETURNTRANSFER => true,
        CURLOPT_HEADER         => false,
        CURLOPT_TIMEOUT        => 20,
        CURLOPT_CONNECTTIMEOUT => 8,
        CURLOPT_HTTPHEADER     => array_merge(['Accept: application/json'], $headers),
    ];
    if ($body !== null) {
        $opts[CURLOPT_POSTFIELDS] = $body;
    }
    curl_setopt_array($ch, $opts);
    $raw  = curl_exec($ch);
    $code = (int)curl_getinfo($ch, CURLINFO_HTTP_CODE);
    $err  = curl_error($ch);
    curl_close($ch);
    $decoded = is_string($raw) ? json_decode($raw, true) : null;
    return [$code, $decoded, $raw, $err ?: null];
}

/**
 * Decode a JWT payload (no signature verification — we just need the `sub`).
 */
function _jwtDecodePayload(string $jwt): ?array {
    $parts = explode('.', $jwt);
    if (count($parts) < 2) return null;
    $payload = $parts[1];
    // Base64URL → base64
    $payload = strtr($payload, '-_', '+/');
    $pad = strlen($payload) % 4;
    if ($pad > 0) $payload .= str_repeat('=', 4 - $pad);
    $raw = base64_decode($payload, true);
    if ($raw === false) return null;
    $obj = json_decode($raw, true);
    return is_array($obj) ? $obj : null;
}

/**
 * Pick the first writable location for local-secrets.php — same list used
 * by cincel-creds-setter.php and config.php.
 */
function _resolveSecretsFile(): array {
    $candidates = [
        __DIR__ . '/../../configurador/php/local-secrets.php',
        __DIR__ . '/../../configurador/php/uploads/voltika-secrets.php',
        __DIR__ . '/../../configurador/php/uploads/firmas/voltika-secrets.php',
        __DIR__ . '/../../configurador/php/contratos/voltika-secrets.php',
        __DIR__ . '/data/voltika-secrets.php',
        sys_get_temp_dir() . '/voltika-secrets.php',
    ];
    foreach ($candidates as $c) {
        $dir = dirname($c);
        if (!is_dir($dir)) @mkdir($dir, 0775, true);
        if (is_dir($dir) && is_writable($dir)) return [$c, true];
        if (file_exists($c) && is_writable($c)) return [$c, true];
    }
    return [$candidates[0], false];
}

function _writeSecretsFile(string $path, string $email, string $password, int $adminId): bool {
    $escEmail = str_replace(["\\", "'"], ["\\\\", "\\'"], $email);
    $escPass  = str_replace(["\\", "'"], ["\\\\", "\\'"], $password);
    $now      = date('Y-m-d H:i:s');
    $contents = <<<PHP
<?php
// Voltika local secrets — auto-generated by cincel-bootstrap.php.
// DO NOT COMMIT this file to git. DO NOT serve it via HTTP directly.
// Last updated: {$now}
// Updated by:   admin id {$adminId}
putenv('CINCEL_EMAIL={$escEmail}');
putenv('CINCEL_PASSWORD={$escPass}');
PHP;
    $bytes = @file_put_contents($path, $contents, LOCK_EX);
    if ($bytes === false) return false;
    @chmod($path, 0640);
    return true;
}


function setMsg(id, text, kind){
  var $el = document.getElementById(id);
  if (!text) { $el.innerHTML = ''; return; }
  var cls = kind === 'ok' ? 'banner-ok' : (kind === 'bad' ? 'banner-bad' : (kind === 'warn' ? 'banner-warn' : 'banner-info'));
  $el.innerHTML = '<div class="banner ' + cls + '">' + text + '</div>';
}


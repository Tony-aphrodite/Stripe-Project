document.getElementById('btnSetPassword').addEventListener('click', function(){
  var email = document.getElementById('bsEmail').value.trim();
  var otp   = document.getElementById('bsOtp').value.trim();
  var pw    = document.getElementById('bsPassword').value;
  if (!email) { setMsg('msg2', 'Email requerido (vuelve al paso 1).', 'bad'); return; }
  if (!/^[0-9]{4,8}$/.test(otp)) { setMsg('msg2', 'OTP debe ser de 4 a 8 dígitos numéricos.', 'bad'); return; }
  if (pw.length < 10) { setMsg('msg2', 'La contraseña debe tener al menos 10 caracteres.', 'bad'); return; }
  var btn = this;
  btn.disabled = true; btn.textContent = '⏳ Validando + estableciendo…';
  setMsg('msg2', '', null);
  fetch(location.pathname, {
    method: 'POST',
    credentials: 'include',
    headers: { 'Content-Type': 'application/json' },
    body: JSON.stringify({ action: 'set_password', email: email, otp_code: otp, password: pw })
  })
  .then(function(r){ return r.json().then(function(j){ return {http: r.status, body: j}; }); })
  .then(function(res){
    btn.disabled = false; btn.textContent = '🔐 Validar OTP y establecer contraseña';
    if (res.body && res.body.ok) {
      var msg = '✅ <strong>' + (res.body.message || 'Listo.') + '</strong>';
      msg += '<br>user_id Cincel: <code>' + res.body.user_id + '</code>';
      msg += '<br>verificación: ' + (res.body.verify_ok ? '✅ JWT obtenido con la nueva contraseña' : '⚠ verificación falló (la contraseña se guardó pero no recibimos JWT al re-validar)');
      setMsg('msg2', msg, 'ok');
      setStep('step2','done');
      setTimeout(function(){
        document.body.insertAdjacentHTML('beforeend', '<div class="banner banner-ok" style="margin-top:14px;font-size:14px;">🎉 <strong>Bootstrap completo.</strong> El servidor ya puede firmar con Cincel sin intervención manual. Próximo paso: actualizar el código de producción (Fase 2) para usar GET /v3/tokens/jwt.</div>');
      }, 200);
    } else {
      var err = (res.body && res.body.error) || ('HTTP ' + res.http);
      var step = res.body && res.body.step ? ' (paso: '+res.body.step+')' : '';
      var detail = res.body && res.body.cincel_body ? JSON.stringify(res.body.cincel_body, null, 2) : '';
      setMsg('msg2', '❌ ' + err + step + (detail ? '<pre>' + detail.replace(/[<>&]/g, function(c){ return {'<':'&lt;','>':'&gt;','&':'&amp;'}[c]; }) + '</pre>' : ''), 'bad');
    }
  })
  .catch(function(e){
    btn.disabled = false; btn.textContent = '🔐 Validar OTP y establecer contraseña';
    setMsg('msg2', '❌ Error de red: ' + e.message, 'bad');
  });
});

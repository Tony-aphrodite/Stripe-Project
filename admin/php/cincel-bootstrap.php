 *
 * One-time tool to bootstrap a permanent Cincel API password.
 *
 * Flow per Cincel's official API guide (GUIA_USO_API_CINCEL.pdf):
 *
 *   Step A — Request OTP from Cincel:
 *     GET https://api.cincel.digital/v3/tokens/otp
 *     Authorization: Basic base64(email:"otp")    ← literal word "otp"
 *     Side effect: Cincel emails a 6-digit code to <email>.
 *
 *   Step B — Validate OTP, retrieve JWT, set password:
 *     1. GET /v3/tokens/jwt with Authorization: Basic base64(email:CODE)
 *        Response includes a JWT.
 *     2. Decode JWT payload, extract `sub` (= user_id).
 *     3. PATCH /v3/users/{user_id} with Authorization: Bearer <jwt>
 *        body: {"password": "<NEW_PASSWORD>"}
 *     4. Write CINCEL_EMAIL + CINCEL_PASSWORD into local-secrets.php
 *        (same file-based override mechanism as cincel-creds-setter.php).
 *
 * Once this completes, production code can authenticate with Basic Auth
 * (email:password) on /v3/tokens/jwt indefinitely — no more OTPs.
 *
 * URL: /admin/php/cincel-bootstrap.php
 */


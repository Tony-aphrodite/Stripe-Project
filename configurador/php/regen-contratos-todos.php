<div class="box">
  <?php if (!$run): ?>
    <p>Click <strong>Regenerar todos</strong> para sobrescribir cada PDF con el template actualizado (Fecha de pago + Punto autorizado + Fecha estimada de entrega).</p>
    <a class="btn" href="?run=1<?= $onePedido ? '&pedido='.urlencode($onePedido) : '' ?><?= $adminKey ? '&admin_key='.urlencode($adminKey) : '' ?>">▶ Regenerar todos</a>
  <?php else: ?>
    <p>Cada PDF de la lista fue reescrito en disco con el template nuevo. Verifica un pedido cualquiera con <code>descargar-contrato.php?pedido=VK-…&inline=1</code>.</p>
  <?php endif; ?>
</div>

<div class="box">
<?php foreach ($results as $r): ?>
  <div class="row">
    <div class="k"><?= htmlspecialchars($r['pedido']) ?></div>
    <div><span class="tag <?= $r['action']==='regenerated'?'regen':($r['action']==='preview'?'preview':'fail') ?>"><?= htmlspecialchars($r['action']) ?></span></div>
    <div class="detail<?= isset($r['error']) ? ' error' : '' ?>">
      <?php if ($r['action']==='preview'): ?>
        tpago=<?= htmlspecialchars($r['tpago']) ?> · punto=<?= htmlspecialchars($r['punto']) ?> · eta=<?= htmlspecialchars($r['eta']) ?>
      <?php elseif (isset($r['error'])): ?>
        ERROR: <?= htmlspecialchars($r['error']) ?>
      <?php else: ?>
        <?= htmlspecialchars($r['path'] ?? '') ?>
      <?php endif; ?>
    </div>
  </div>
<?php endforeach; ?>
</div>

